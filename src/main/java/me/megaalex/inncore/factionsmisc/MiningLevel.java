package me.megaalex.inncore.factionsmisc;

public class MiningLevel {

    int x;
    int y;

    public MiningLevel(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
